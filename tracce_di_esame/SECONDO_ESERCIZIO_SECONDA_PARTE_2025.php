    <h2> SPIEGAZIONE DEL CONCETTO DI RAGGRUPPAMENTO E DELLA CLAUSOLA HAVING</h2><br>
    <br>
    <p>
        il raggurppamento avviene con GROUP BY e serve per aggreare i dati <br>
        in base a uno o più campi <br>
        la funzione di aggregazione operano su molte più righe e restituiscono un singolo valore <br>
        es' abbimao count -> che conta il num di righe di un gruppo <br>
        sum-> che somma i val' di una colonna ecc... <br>
        HAVING INVECE  si usa per filtrare i risultati dopo l'aggregazione appena spiegata<br>
        però where in questo caso non può essere usato percheè<br>
        - where filtra i dati prima del raggruppamento <br>
        - having inevce ha la possibilità di filtrare i dati dopo l'aggregazione.<br>
        
    </p>