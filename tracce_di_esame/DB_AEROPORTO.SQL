CREATE DATABASE IF NOT EXISTS aeroporti;
USE aeroporti;

CREATE TABLE IF NOT EXISTS AEROPORTO (
    Città VARCHAR(100) PRIMARY KEY,
    Nazione VARCHAR(100) NOT NULL,
    NumPiste INT NULL
);

CREATE TABLE IF NOT EXISTS AEREO (
    TipoAereo VARCHAR(50) PRIMARY KEY,
    NumPasseggeri INT NOT NULL,
    QtaMerci INT NOT NULL
);

CREATE TABLE IF NOT EXISTS VOLO (
    IdVolo VARCHAR(10) PRIMARY KEY,
    GiornoSett VARCHAR(20) NOT NULL,
    CittàPar VARCHAR(100) NOT NULL,
    OraPart TIME NOT NULL,
    CittàArr VARCHAR(100) NOT NULL,
    OraArr TIME NOT NULL,
    TipoAereo VARCHAR(50) NOT NULL,
    FOREIGN KEY (CittàPar) REFERENCES AEROPORTO(Città),
    FOREIGN KEY (CittàArr) REFERENCES AEROPORTO(Città),
    FOREIGN KEY (TipoAereo) REFERENCES AEREO(TipoAereo)
);
